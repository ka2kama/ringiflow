// sqlx::migrate!("../../migrations") マクロはコンパイル時にマイグレーションファイルを埋め込む。
// しかし Cargo はディレクトリへのファイル追加を自動追跡しないため、
// 新しいマイグレーションファイルが追加されても再コンパイルされない場合がある。
// （特に sccache 等のキャッシュ環境で問題になる）
//
// この build.rs でマイグレーションディレクトリの変更を追跡し、
// ファイル追加時に infra クレートの再コンパイルを強制する。
fn main() {
    println!("cargo::rerun-if-changed=../../migrations");
}
