//! # 日時ユーティリティ
//!
//! アプリケーション全体で一貫した日時操作を提供する。
//!
//! ## 設計方針
//!
//! - すべての日時は UTC で扱う（タイムゾーン変換は表示層で行う）
//! - `chrono` クレートを使用し、型安全な日時操作を提供
//!
//! ## 将来の拡張
//!
//! - テスト時に固定日時を注入できる仕組み（Clock トレイト）
//! - 日時フォーマットのユーティリティ

use chrono::{DateTime, Utc};

/// 現在の UTC 日時を取得する
///
/// データベースへの保存や、イベントのタイムスタンプ生成に使用する。
///
/// # 例
///
/// ```rust
/// use ringiflow_shared::datetime;
///
/// let now = datetime::now();
/// println!("現在時刻: {}", now);
/// ```
///
/// # 注意
///
/// 本番コードでは直接この関数を呼び出すのではなく、
/// 依存性注入パターン（Clock トレイト）の導入を検討すること。
/// これにより、テスト時に固定日時を使用できるようになる。
pub fn now() -> DateTime<Utc> {
   Utc::now()
}
